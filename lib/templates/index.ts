/**
 * CSV template generation.
 *
 * These functions produce the exact bytes that the upload parsers expect.
 * Every design decision here is driven by parser compatibility:
 *
 *   - Column names match the canonical keys in HEADER_MAP / GRADE_HEADER_MAP
 *     exactly, so normaliseHeader() maps them without any alias lookup.
 *
 *   - Sample rows use values that pass every Zod rule in the validators,
 *     so users can upload the unmodified template as a smoke-test.
 *
 *   - Comment rows begin with "#" — the upload routes strip them before
 *     passing text to parseStudentCSV / parseGradeCSV, so users do NOT
 *     need to delete them. We keep them for human readability.
 *
 *   - UTF-8 BOM (\uFEFF) is prepended so Excel on Windows opens the file
 *     with correct character encoding without any manual import steps.
 *     The parsers already strip the BOM on receipt.
 *
 *   - CRLF (\r\n) line endings because Excel on Windows writes CRLF and
 *     many institutional IT environments reject LF-only CSV files.
 *     The parsers handle both endings.
 *
 *   - Fields that contain commas are quoted. Fields that contain quotes
 *     have the quote doubled. No field in these templates currently needs
 *     quoting, but the helper is applied uniformly for safety.
 *
 * Nothing in this file imports from DB or Next.js — safe to call from
 * API routes, server actions, and unit tests.
 */

import { DEFAULT_GRADE_SCALE } from "@/lib/gpa/scale";

// ─── Shared CSV utilities ─────────────────────────────────────────────────────

const CRLF = "\r\n";
const BOM = "\uFEFF";

/** Quote a CSV field if it contains commas, double-quotes, or newlines. */
function csvField(value: string): string {
  if (value.includes(",") || value.includes('"') || value.includes("\n")) {
    return `"${value.replace(/"/g, '""')}"`;
  }
  return value;
}

/** Join an array of fields into one CSV row string (no line ending). */
function csvRow(fields: string[]): string {
  return fields.map(csvField).join(",");
}

/** Join lines with CRLF and prepend UTF-8 BOM. */
function buildCSV(lines: string[]): string {
  return BOM + lines.join(CRLF) + CRLF;
}

// ─── Shared comment block builder ─────────────────────────────────────────────

function commentBlock(lines: string[]): string[] {
  return lines.map((l) => (l === "" ? "#" : `# ${l}`));
}

