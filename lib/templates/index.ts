const STUDENT_COMMENTS = commentBlock([
  "STUDENT BULK UPLOAD TEMPLATE",
  "Generated by the Transcript Management System",
  "",
  "INSTRUCTIONS:",
  "  1. Replace the sample rows below with your student data.",
  "  2. Do NOT modify the header row (row after these comments).",
  "  3. Comment rows (starting with #) are stripped automatically on upload.",
  "  4. programmeCode must exactly match an active programme in the system.",
  "  5. level must be one of: 100, 200, 300, 400, 500, 600, 700, 800",
  "  6. entryYear must be a 4-digit year, e.g. 2024",
  "  7. graduationYear is optional — leave the cell blank for current students.",
  "  8. indexNumber must be unique across all students in the system.",
  "",
  "COLUMN REFERENCE:",
  "  indexNumber    | Required | Unique student ID, e.g. CS/2024/001",
  "  firstName      | Required | Student's given name",
  "  lastName       | Required | Student's family/surname",
  "  programmeCode  | Required | Must match an existing active programme code",
  "  level          | Required | Academic level: 100, 200, 300, 400, 500, 600, 700, 800",
  "  entryYear      | Required | 4-digit year of first admission, e.g. 2024",
  "  graduationYear | Optional | 4-digit graduation year — blank for current students",
]);

export function generateStudentTemplate(): string {
  const lines = [
    ...STUDENT_COMMENTS,
    csvRow([...STUDENT_HEADERS]),
    ...STUDENT_SAMPLE_ROWS.map((r) =>
      csvRow([
        r.indexNumber,
        r.firstName,
        r.lastName,
        r.programmeCode,
        r.level,
        r.entryYear,
        r.graduationYear,
      ]),
    ),
  ];

  return buildCSV(lines);
}

